import os
import glob
import pandas as pd
import xml.etree.ElementTree as ET


def xml_to_csv(path):
    xml_list = []
    for xml_file in glob.glob(path + '/*.xml'):
        tree = ET.parse(xml_file)
        root = tree.getroot()
        for member in root.findall('object'):
            value = (root.find('filename').text,
                     int(root.find('size')[0].text),
                     int(root.find('size')[1].text),
                     member[0].text,
                     int(member[4][0].text),
                     int(member[4][1].text),
                     int(member[4][2].text),
                     int(member[4][3].text)
                     )
            xml_list.append(value)
    column_name = ['filename', 'width', 'height',
                   'class', 'xmin', 'ymin', 'xmax', 'ymax']
    xml_df = pd.DataFrame(xml_list, columns=column_name)
    return xml_df


def run():
    for dir in ['train_xml_labels', 'test_xml_labels']:
        image_path = os.path.join(os.getcwd(), 'images/{}'.format(dir))
        parsed_xml = xml_to_csv(image_path)
        parsed_xml.to_csv('images/{}.csv'.format(
            dir.replace('xml', 'csv')), index=None)
        print('Converted xml to csv.')


run()
